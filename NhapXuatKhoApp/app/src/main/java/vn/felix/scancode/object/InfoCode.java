public class InfoCode {
    private String stampCode;
    private String serialCode;
    private String dateScan;
    private String plotsCode;
    private String nameProduce;

    public String getStampCode() {
        return stampCode;
    }

    public void setStampCode(String stampCode) {
        this.stampCode = stampCode;
    }

    public String getSerialCode() {
        return serialCode;
    }

    public void setSerialCode(String serialCode) {
        this.serialCode = serialCode;
    }

    public String getDateScan() {
        return dateScan;
    }

    public void setDateScan(String dateScan) {
        this.dateScan = dateScan;
    }

    public String getPlotsCode() {
        return plotsCode;
    }

    public void setPlotsCode(String plotsCode) {
        this.plotsCode = plotsCode;
    }

    public String getNameProduce() {
        return nameProduce;
    }

    public void setNameProduce(String nameProduce) {
        this.nameProduce = nameProduce;
    }
}
